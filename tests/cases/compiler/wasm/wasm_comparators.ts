export function equals(a: number, b: number) { return a == b; }
export function greaterThan(a: number, b: number) { return a > b; }
export function lessThan(a: number, b: number) { return a < b; }
export function greaterThanEquals(a: number, b: number) { return a >= b; }
export function lessThanEquals(a: number, b: number) { return a <= b; }
